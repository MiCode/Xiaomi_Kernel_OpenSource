int ktz8866_reg_write_bytes(unsigned char addr, unsigned char value)
{
	int ret = -EINVAL;
	char write_data[2] = { 0 };

	if (g_ktz8866_led.client == NULL) {
		pr_info("ERROR!! ktz8866 i2c client is null\n");
		return ret;
	}

	if (addr < KTZ8866_DISP_REV || addr > KTZ8866_DISP_FULL_CURRENT) {
		pr_info("ERROR!! ktz8866 addr overflow\n");
		return ret;
	}

	write_data[0] = addr;
	write_data[1] = value;
	ret = i2c_master_send(g_ktz8866_led.client, write_data, 2);
	if (ret < 0)
		pr_info("ktz8866 write data fail !!\n");

	return ret;
}
EXPORT_SYMBOL(ktz8866_reg_write_bytes);

int ktz8866_reg_read_bytes(char addr, char *buf)
{
	int ret = -EINVAL;
	char puReadCmd[1] = {addr};

	if (g_ktz8866_led.client == NULL && buf == NULL) {
		pr_info("ERROR!! ktz8866 i2c client or buffer is null\n");
		return ret;
	}

	if (addr < KTZ8866_DISP_REV || addr > KTZ8866_DISP_FULL_CURRENT) {
		pr_info("ERROR!! ktz8866 addr overflow\n");
		return ret;
	}

	ret = i2c_master_send(g_ktz8866_led.client, puReadCmd, 1);
	if (ret < 0) {
		pr_info(" ktz8866 write failed!!\n");
		return ret;
	}

	ret = i2c_master_recv(g_ktz8866_led.client, buf, 1);
	if (ret < 0) {
		pr_info(" ktz8866 read failed!!\n");
		return ret;
	}

	return ret;
}
EXPORT_SYMBOL(ktz8866_reg_read_bytes);

