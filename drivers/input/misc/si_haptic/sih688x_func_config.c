void sih_load_reg_config(sih_haptic_t *sih_haptic, uint8_t func_type)
{
	uint8_t i;
	reg_format_t *reg_list = NULL;

	for (i = 0; i < sizeof(func_list) / sizeof(lra_reg_func_t); i++) {
		if (strncmp(func_list[i].lra_name, sih_haptic->chip_attr.lra_name,
			SIH_LRA_NAME_LEN) == 0) {
			/* match reg_list */
			switch (func_type) {
			case REG_FUNC_CONT:
				reg_list = func_list[i].reg_cont_list;
				break;
			case REG_FUNC_RL:
				reg_list = func_list[i].reg_rl_list;
				break;
			case REG_FUNC_VBAT:
				reg_list = func_list[i].reg_vbat_list;
				break;
			default:
				hp_err("%s:no func match\n", __func__);
				break;
			}
			break;
		}
	}

	if (!reg_list) {
		hp_err("%s:no reg list match\n", __func__);
		return;
	}

	for (i = 0; i < HAPTIC_CONFIG_MAX_REG_NUM; i++) {
		if (reg_list[i].operation == OPERATION_END)
			break;
		switch (reg_list[i].operation) {
		case OPERATION_WRITE:
			haptic_regmap_write(sih_haptic->regmapp.regmapping,
				reg_list[i].addr, SIH_I2C_OPERA_BYTE_ONE, &reg_list[i].val);
			hp_info("%s:write 0x%02x:0x%02x\n", __func__,
				reg_list[i].addr, reg_list[i].val);
			break;
		case OPERATION_READ:
			haptic_regmap_read(sih_haptic->regmapp.regmapping,
				reg_list[i].addr, SIH_I2C_OPERA_BYTE_ONE, &reg_list[i].val);
			hp_info("%s:read 0x%02x:0x%02x\n", __func__,
				reg_list[i].addr, reg_list[i].val);
			break;
		case OPERATION_BIT:
			haptic_regmap_update_bits(sih_haptic->regmapp.regmapping,
				reg_list[i].addr, reg_list[i].mask, reg_list[i].val);
			hp_info("%s:bit 0x%02x:0x%02x\n", __func__,
				reg_list[i].addr, reg_list[i].val);
			break;
		default:
			hp_err("%s:operation err %d\n", __func__, reg_list[i].operation);
			break;
		}
	}
}

void sih_reg_config_parse(sih_haptic_t *sih_haptic, 
	haptic_reg_config_t *reg_config)
{
	uint8_t reg_addr = 0;
	uint8_t reg_value = 0;
	int i;

	for (i = 0; i < reg_config->reg_num; i++) {
		reg_addr = reg_config->reg_cont[i].reg_addr;
		reg_value = reg_config->reg_cont[i].reg_value;
		haptic_regmap_write(sih_haptic->regmapp.regmapping,
			reg_addr, SIH_I2C_OPERA_BYTE_ONE, &reg_value);
		hp_info("%s:0x%02x:0x%02x\n", __func__, reg_addr, reg_value);
	}
}

int sih_lra_config_load(sih_haptic_t *sih_haptic)
{
	int i;

	hp_info("%s:enter\n", __func__);
	for (i = 0; i < sizeof(config_list) / sizeof(lra_reg_config_t); i++) {
		if (strncmp(config_list[i].lra_name, sih_haptic->chip_attr.lra_name,
			SIH_LRA_NAME_LEN) == 0) {
			/* load func */
			sih_reg_config_parse(sih_haptic, config_list[i].reg_config_list);
			return 0;
		}
	}
	hp_err("%s:no match lra config\n", __func__);
	return -1;
}